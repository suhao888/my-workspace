class FieldMapper:
    """
    字段映射器
    将客户科目名称映射为引擎标准名称
    """

    def __init__(self, custom_map: Dict[str, str] = None):
        self._map = DEFAULT_TB_MAP.copy()
        if custom_map:
            self._map.update(custom_map)

    def map(self, name: str) -> str:
        """映射单个科目名称"""
        return self._map.get(name, name)

    def map_dict(self, source: Dict[str, float]) -> Dict[str, float]:
        """映射整个科目字典"""
        result = {}
        for raw_name, amount in source.items():
            std_name = self.map(raw_name)
            result[std_name] = result.get(std_name, 0) + amount
        return result
